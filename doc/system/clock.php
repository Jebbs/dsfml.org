            <section id="module_content"><section class="section ddoc_sections">
  <div class="ddoc_summary">
  <p class="para">
    Clock is a lightweight class for measuring time.

  </p>
</div>
<div class="ddoc_description">
  <p class="para">
    Its provides the most precise time that the underlying OS can achieve
 (generally microseconds or nanoseconds). It also ensures monotonicity, which
 means that the returned time can never go backward, even if the system time
 is changed.


  </p>
</div>
<div class="ddoc_section">
  <p class="para">
    <span class="ddoc_section_h">Example:</span>

<section class="code_listing">
  <div class="code_sample">
    <pre><code class="prettyprint">auto <span class="psymbol">clock</span> = Clock();
...
Time duration1 = <span class="psymbol">clock</span>.getElapsedTime();
...
Time duration2 = <span class="psymbol">clock</span>.restart();
</code></pre>
  </div>
</section>
<br><br>
 <p class="para">The Time value returned by the clock can then be converted to a number
 of seconds, milliseconds or even microseconds.</p>


  </p>
</div>
