            <h1 class="module_name">dsfml.system.vector3</h1>
            <section id="module_content"><section class="section ddoc_sections">
  <div class="ddoc_summary">
  <p class="para">
    Vector3 is a simple structure that defines a mathematical vector with three
 coordinates (x, y and z). It can be used to represent anything that has three
 dimensions: a size, a point, a velocity, etc.

  </p>
</div>
<div class="ddoc_description">
  <p class="para">
    The template parameter <code class="prettyprint">T</code> is the type of the coordinates. It can be any type
 that supports arithmetic operations (+, -, /, *) and comparisons (==, !=),
 for example int or float.
<br><br>
 You generally don't have to care about the templated form (Vector2!(T)),
 the most common specializations have special aliases:
 <ul class="lists"> <li>Vector3!(float) is Vector2f</li>
 <li>Vector3!(int) is Vector2i</li></ul>


  </p>
</div>
<div class="ddoc_section">
  <p class="para">
    <span class="ddoc_section_h">Example:</span>

<section class="code_listing">
  <div class="code_sample">
    <pre><code class="prettyprint">auto v1 = Vector3f(16.5f, 24.f, -8.2f);
v1.x = 18.2f;
float y = v1.y;
float z = v1.z;

auto v2 = v1 * 5.f;
Vector3f v3;
v3 = v1 + v2;

bool different = (v2 != v3);
</code></pre>
  </div>
</section>


  </p>
