            <h1 class="module_name">dsfml.window.event</h1>
            <section id="module_content"><section class="section ddoc_sections">
  <div class="ddoc_summary">
  <p class="para">
    <u>Event</u> holds all the informations about a system event that just
 happened. Events are retrieved using the <code class="prettyprint">Window.pollEvent</code> and
 <code class="prettyprint">Window.waitEvent</code> functions.

  </p>
</div>
<div class="ddoc_description">
  <p class="para">
    An <u>Event</u> instance contains the type of the event (mouse moved, key
 pressed, window closed, ...) as well as the details about this particular
 event. Please note that the event parameters are defined in a union, which
 means that only the member matching the type of the event will be properly
 filled; all other* members will have undefined values and must not be read if
 the type of the event doesn't match. For example, if you received a
 <code class="prettyprint">KeyPressed</code> event, then you must read the <code class="prettyprint">event.key</code> member, all other
 members such as event.MouseMove or <code class="prettyprint">event.text</code> will have undefined values.


  </p>
</div>
<div class="ddoc_section">
  <p class="para">
    <span class="ddoc_section_h">Example:</span>

<section class="code_listing">
  <div class="code_sample">
    <pre><code class="prettyprint">Event <span class="psymbol">event</span>;
while (window.pollEvent(<span class="psymbol">event</span>))
{
    // Request for closing the window
    if (<span class="psymbol">event</span>.type == Event.EventType.Closed)
        window.close();

    // The escape key was pressed
    if ((<span class="psymbol">event</span>.type == Event.EventType.KeyPressed) &amp;&amp; (<span class="psymbol">event</span>.key.code == Keyboard.Key.Escape))
        window.close();

    // The window was resized
    if (<span class="psymbol">event</span>.type == Event.EventType.Resized)
        doSomethingWithTheNewSize(<span class="psymbol">event</span>.size.width, <span class="psymbol">event</span>.size.height);

    // etc ...
}
</code></pre>
  </div>
</section>

  </p>
</div>

